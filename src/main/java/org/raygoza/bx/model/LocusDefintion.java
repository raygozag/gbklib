package org.raygoza.bx.model;

public class LocusDefintion {

	private String name;
	private String sequenceLength;
	private String moleculeType;
	private String genbankDivision;
	private String modificationDate;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSequenceLength() {
		return sequenceLength;
	}
	public void setSequenceLength(String sequenceLength) {
		this.sequenceLength = sequenceLength;
	}
	public String getMoleculeType() {
		return moleculeType;
	}
	public void setMoleculeType(String moleculeType) {
		this.moleculeType = moleculeType;
	}
	public String getGenbankDivision() {
		return genbankDivision;
	}
	public void setGenbankDivision(String genbankDivision) {
		this.genbankDivision = genbankDivision;
	}
	public String getModificationDate() {
		return modificationDate;
	}
	public void setModificationDate(String modificationDate) {
		this.modificationDate = modificationDate;
	}
	
	
	
}
